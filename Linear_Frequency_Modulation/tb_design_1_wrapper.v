`timescale 1ns / 1ps

module tb_design_1_wrapper();

    // Inputs
    reg sys_clock;
    reg reset_rtl;
    reg enable_0;

    // Outputs (AXI-Stream ??????????? Vivado ????? ?????????? ???????)
    wire [31:0] M_AXIS_DATA_0_tdata;
    wire        M_AXIS_DATA_0_tvalid;
    wire [31:0] M_AXIS_PHASE_0_tdata;
    wire        M_AXIS_PHASE_0_tvalid;

    // Instantiate the Block Design Wrapper
    design_1_wrapper uut (
        .sys_clock(sys_clock),
        .reset_rtl(reset_rtl),
        .enable_0(enable_0),
        
        .M_AXIS_DATA_0_tdata(M_AXIS_DATA_0_tdata),
        .M_AXIS_DATA_0_tvalid(M_AXIS_DATA_0_tvalid),
        
        .M_AXIS_PHASE_0_tdata(M_AXIS_PHASE_0_tdata),
        .M_AXIS_PHASE_0_tvalid(M_AXIS_PHASE_0_tvalid)
    );

    // 100MHz System Clock Generation
    always #5 sys_clock = ~sys_clock; 

    initial begin
        // Initialize Inputs
        sys_clock = 0;
        reset_rtl = 1; // Clocking Wizard reset ?????????? Active High ???
        enable_0 = 0;

        // Reset Sequence
        #100;
        reset_rtl = 0; // ???????? ??????????
        
        // Clocking Wizard ?? Processor System Reset ?? ?????????????????? (Lock ????) ???? ????????
        #2000; 
        
        // LFM Chirp ?????? ?????????????
        enable_0 = 1;

        // ???? ??? ????????????? ??????? ???? ???????? ?? ??????????
        #50000;
        
        $finish; // ???????? ??????????????????
    end

endmodule
